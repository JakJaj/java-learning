package Model;

public class Album {
    private int id;
    private String name;
    private int atrist;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAtrist() {
        return atrist;
    }

    public void setAtrist(int atrist) {
        this.atrist = atrist;
    }
}
